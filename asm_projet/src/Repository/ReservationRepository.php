<?php

namespace App\Repository;

use App\Entity\Reservation;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Reservation>
 *
 * @method Reservation|null find($id, $lockMode = null, $lockVersion = null)
 * @method Reservation|null findOneBy(array $criteria, array $orderBy = null)
 * @method Reservation[]    findAll()
 * @method Reservation[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class ReservationRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Reservation::class);
    }

    public function add(Reservation $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Reservation $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function verif_disp_reservation($id_livre,$date_debut,$date_retour){

        $query = $this->createQueryBuilder('r')
                ->where('r.livre = :id_livre')
                ->setParameter('id_livre', $id_livre)
                ->andWhere('
                            :date_debut between r.date_reservation AND r.date_fin  OR 
                            :date_retour between r.date_reservation AND r.date_fin OR
                            r.date_reservation between :date_debut AND :date_retour OR 
                            r.date_fin between :date_debut AND :date_retour'
                )
                ->setParameter('date_debut', $date_debut)
                ->setParameter('date_retour', $date_retour)
                ->getQuery()
                ->getResult()
                ;
                return $query;

    } 











